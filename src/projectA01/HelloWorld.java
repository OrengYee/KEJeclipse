package projectA01;

public class HelloWorld {
	/*
	 * print 와 println 의 차이 
	 * println() 메소드와는 달리 print() 메소드는 전달된 데이터를 출력한 후에 줄 바꿈을 하지 않습니다.

	 * 
	 */

	public static void main(String[] args) {
		System.out.print("안녕"); //print문은 옆으로 출력이 됨
		System.out.print("   ");
		System.out.print("하세요");
		System.out.print("\n");   // 밑으로 문장이 내려감
		System.out.println("안녕 하세요");
		System.out.print("반가워요 ");
		System.out.println("즐거운 목요일이네요");
		

	}

}
